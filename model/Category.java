package model;

import java.io.Serializable;
import java.util.Collection;
import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.OneToMany;
import javax.persistence.Table;
import javax.xml.bind.annotation.XmlRootElement;
import javax.xml.bind.annotation.XmlTransient;

/**
 *
 * @author Programador
 */
@Entity
@Table(name = "category")
@XmlRootElement
@NamedQueries({
    @NamedQuery(name = "Category.findAll", query = "SELECT c FROM Category c")
    , @NamedQuery(name = "Category.findByCode", query = "SELECT c FROM Category c WHERE c.code = :code")
    , @NamedQuery(name = "Category.findByDescription", query = "SELECT c FROM Category c WHERE c.description = :description")})
public class Category implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Basic(optional = false)
    @Column(name = "code")
    private Integer code;
    @Column(name = "description")
    private String description;
    @OneToMany(mappedBy = "codeCategory")
    private Collection<Item> itemCollection;
    @OneToMany(mappedBy = "codeCategory")
    private Collection<Filterhistory> filterhistoryCollection;
    @OneToMany(mappedBy = "codeCategory")
    private Collection<Additional> additionalCollection;

    public Category() {
    }

    public Category(Integer code) {
        this.code = code;
    }

    public Integer getCode() {
        return code;
    }

    public void setCode(Integer code) {
        this.code = code;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    @XmlTransient
    public Collection<Item> getItemCollection() {
        return itemCollection;
    }

    public void setItemCollection(Collection<Item> itemCollection) {
        this.itemCollection = itemCollection;
    }

    @XmlTransient
    public Collection<Filterhistory> getFilterhistoryCollection() {
        return filterhistoryCollection;
    }

    public void setFilterhistoryCollection(Collection<Filterhistory> filterhistoryCollection) {
        this.filterhistoryCollection = filterhistoryCollection;
    }

    @XmlTransient
    public Collection<Additional> getAdditionalCollection() {
        return additionalCollection;
    }

    public void setAdditionalCollection(Collection<Additional> additionalCollection) {
        this.additionalCollection = additionalCollection;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (code != null ? code.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof Category)) {
            return false;
        }
        Category other = (Category) object;
        if ((this.code == null && other.code != null) || (this.code != null && !this.code.equals(other.code))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "model.Category[ code=" + code + " ]";
    }
    
}
